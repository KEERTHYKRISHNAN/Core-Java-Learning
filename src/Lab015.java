package src;

public class Lab015 {
    public static void main(String[] args) {
        //math operators/arithmetic

        int a = 10;
        int b = 20;
        int sum = a+b;
        System.out.println(sum);

        int sub = a-b;
        System.out.println(sub);

        int mul = a*b;
        System.out.println(mul);

        int div = a/b;
        System.out.println(div);

        //using double will showcast the values with decimal
        double c =10;
        double d = 20;
        double divd = c/d;
        System.out.println(divd);
    }
}
