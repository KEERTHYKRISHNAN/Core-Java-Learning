package Casting;

public class Lab038 {

    //Explicit Casting

    public static void main(String[] args) {

        int a1 = 450;
        byte a2 = (byte) a1;
        System.out.println(a2);

        double a3 = (double) a1;
        System.out.println(a3);

        byte b = 23;
        int a = (int)b;
        System.out.println(a );

    }
}
