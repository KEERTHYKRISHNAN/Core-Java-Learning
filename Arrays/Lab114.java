package Arrays;

public class Lab114 {
    public static void main(String[] args) {
        //using arithmetic in  array

        int [] arr1 = {10,20,30,40};
        for (int i = 0; i < arr1.length; i++) {
            System.out.println(arr1[i]*2);

            System.out.println(arr1[i]+5);

            System.out.println(arr1[i]-2);

            System.out.println(arr1[i]/2);

        }
    }
}
