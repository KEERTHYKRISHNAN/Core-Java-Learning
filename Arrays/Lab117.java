package Arrays;

public class Lab117 {
    public static void main(String[] args) {
        //WhileLoop

        int [] marks = new int[4];
        marks [0] = 98;
        marks [1] = 99;
        marks [2] = 87;
        marks [3] = 78;

        int i = 0;
        while (i< marks.length){
            System.out.println(i);
            i++;
        }

        System.out.println("----End of the Program----");
    }
}
