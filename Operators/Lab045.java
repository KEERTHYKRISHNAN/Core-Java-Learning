package Operators;

public class Lab045 {
    public static void main(String[] args) {
        // Ternary Operator - condition?
        // condition is true do this : (else) if false do this

        int a = (10>20) ? 10 : 20;
        System.out.println(a);

        String s1 = (10>20) ? "10" : "TEWENTY";
        System.out.println(s1);
    }
}
