package Operators;

public class Lab023 {
    public static void main(String[] args) {

        //Binary Arithmetic Operator
        //Byte +Byte = Integer

        byte a = 10;
        byte b = 20;
        System.out.println(a+b); // the result type--> value will be integer
                                // in System.out.println
                                // the bigger value will be result
                                // example int+long= long
                                // byte + double = double
                                // int + long = long
                                // short + float = float
                                // char + double = double
        int c = 10;
        long d = 9585l;
        System.out.println(c+d);

        System.out.println();

        short e = 230;
        float f = 10f;
        System.out.println(e+f);

    }
}
